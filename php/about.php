			<p>ನಲವತ್ತರ ದಶಕದ ಆದಿಭಾಗದಲ್ಲಿ ಕನ್ನಡದ ನೆಲದಲ್ಲಿ ಮೂಡಿಬಂದ &ldquo;ಕೊರವಂಜಿ&rdquo; ಎಂಬ ನಗೆ ಮಾಸಪತ್ರಿಕೆಯನ್ನು ಒಂದು ವಿಧದಲ್ಲಿ &ldquo;ಆಧುನಿಕ
			ನಗೆಪ್ರಜ್ಞೆಯ ಹರಿಕಾರಿಣಿ&rdquo; ಎಂದು ಕರೆದರೆ ತಪ್ಪಾಗಲಾರದು. ರಾ.ಶಿ. ಎಂಬ ಕಾವ್ಯನಾಮದಿಂದ ನಾಡಿನ ಜನತೆಗೆ ಸುಪರಿಚಿತರಾದ ಎಂ.ಶಿವರಾಂ,
			ಕೊರವಂಜಿಯ ಸಂಸ್ಥಾಪಕರು. ಮೂವತ್ತರ ದಶಕದಲ್ಲಿ ಅವರ ಮಿತ್ರ ಅಶ್ವಥ್ಥನಾರಾಯಣರಾಯರು ಹೊರತರುತ್ತಿದ್ದ &ldquo;ನವಜೀವನ&rdquo; ಎಂಬ ದಿನಪತ್ರಿಕೆಯಲ್ಲಿ, &ldquo;ಸೋಮವಾರದ ಸುಸುದ್ದಿ&rdquo; ಎಂಬ ಶೀರ್ಷಿಕೆಯಡಿಯಲ್ಲಿ
			ನಗೆಚಟಾಕಿ, ಕುಹಕಿಡಿಗಳನ್ನು ರಾ.ಶಿ. ಬರೆಯಲು ಪ್ರಾರಂಭಿಸಿದರು.</p><br />
			
			<p>ಕೈಲಾಸಂ ಅವರೊಂದಿಗಿನ ಒಡನಾಟ. ರಾ.ಶಿ. ಯವರ ಹಾಸ್ಯ ಮನೋವೃತ್ತಿ ಹೊಳಪು ಪಡೆಯಲು ಇನ್ನೊಂದು ಕಾರಣ. ಕೈಲಾಸಂ ಜೊತೆಗಿನ ಸಂಪರ್ಕ
			ರಾ.ಶಿ. ಯವರ ಜೀವನದೃಷ್ಟಿಯನ್ನೇ ಬದಲಿಸುವಂತಹ ಪರಿಣಾಮ ಬೀರಿತ್ತು. ಕೈಲಾಸಂ ಅವರೊಡನೆ ಕಂಟೋನ್ಮೆಂಟ್ಗೆ ಸೈಕಲ್ಲಿನಲ್ಲಿ ಸವಾರಿ. ದಾರಿಯುದ್ದಕ್ಕೂ ಮಾತುಕತೆ. ಅನೇಕ ಬಾರಿ, ಕಂಟೋನ್ಮೆಂಟಿನ ಪೆರೇಡ್
			ಗ್ರೌಂಡ್ಸ್ ಹತ್ತಿರ ಇದ್ದ ಚರ್ಚಿನ ಆವರಣದಲ್ಲಿ ಕುಳಿತು, ಏಕಲವ್ಯ, ಮಾರ್ಕಾಂಡೇಯ, ಕರ್ಣ ಇವರುಗಳ ಬಗ್ಗೆ ಕೈಲಾಸಂ ಕಥನಮಾಡುತ್ತಿದ್ದಾಗ ರಾ.ಶಿ. ತಲ್ಲೀನರಾಗಿ ಕೇಳುತ್ತಿದ್ದರಂತೆ. &ldquo;ಕೈಲಾಸಂ ಅವರ ಮಾರ್ಕಾಂಡೇಯ
			ಚಿತ್ರಣ ನನ್ನ ವ್ಯಕ್ತಿತ್ವವನ್ನೇ ಬದಲಿಸಿತು. ಅಹಂಕಾರ, ಸ್ವಾರ್ಥ ಸ್ವಲ್ಪ ಕರಗಿ ನಾನು ಮನುಷ್ಯವರ್ಗಕ್ಕೆ ಸೇರುವಂತಾದೆ&rdquo; ಎನ್ನುತ್ತಾರೆ ರಾ.ಶಿ.</p><br />

			<p>೧೯೪೨ರಿಂದ ಸತತವಾಗಿ ಇಪ್ಪತ್ತೈದು ವರ್ಷಗಳ ಕಾಲ ನಾಡಿನ ಹಾಸ್ಯರಸಿಕರನ್ನು ರಂಜಿಸಿದ ಕೊರವಂಜಿಯ ಹುಟ್ಟಿಗೆ ರಾ.ಶಿ. ಯವರೊಂದಿಗೆ ಒತ್ತಾಸೆಯಾಗಿ ನಿಂತವರು, ನಾ.ಕಸ್ತೂರಿಯವರು. ಸಮಾನ ಮನೋಧರ್ಮದ
			ಇವರಿಬ್ಬರ ಭೇಟಿ ಕನ್ನಡ ನಗೆ ಸಾಹಿತ್ಯಕ್ಕೆ ಒಂದು ಮಹತ್ವದ ಘಟನೆಯಾಗಿ ಪರಿಣಮಿಸಿತು. ಖ್ಯಾತ ವ್ಯಂಗ್ಯಚಿತ್ರಕಾರ ಆರ್.ಕೆ.ಲಕ್ಷ್ಮಣ್ ಆ ದಿನಗಳಲ್ಲಿ ಕಸ್ತೂರಿಯವರ ವಿದ್ಯಾರ್ಥಿ. ಲಕ್ಷ್ಮಣ್ ಚಿತ್ರಗಳು, 
			ರಾ.ಶಿ., ಕಸ್ತೂರಿ, ಇವರುಗಳ ನಗೆಲೇಖನಗಳೊಂದಿಗೆ ೧೯೪೨ರ ಕಾಮನಹಬ್ಬದಂದು (ಮಾರ್ಚಿ ೧೮) ಜನ್ಮತಾಳಿದ ಕೊರವಂಜಿಗೆ ಆ ಯುದ್ಧದ ದಿನಗಳಲ್ಲೂ ಒಳ್ಳೆಯ ಭವಿಷ್ಯ ಇರುವ
			ಸೂಚನೆಗಳಿದ್ದವು. ಹೇಳಿಕೊಳ್ಳುವಂತಹ ಯಾವ ಹಾಸ್ಯಸಾಹಿತ್ಯವೂ ಇಲ್ಲದ ಆ ಕಾಲದಲ್ಲಿ ಸಹಜವಾಗಿಯೇ ನಾಡಿನ ಓದುಗರು ಕೊರವಂಜಿಯನ್ನು ಆದರದಿಂದ ಬರಮಾಡಿಕೊಂಡರು.</p><br />
			
