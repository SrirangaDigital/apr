			ಪ್ರಾರಂಭದಲ್ಲಿ ರಾ.ಶಿ., ನಾ. ಕಸ್ತೂರಿ ಅವರುಗಳ ಲೇಖನಗಳೇ ತುಂಬಿರುತ್ತಿದ್ದ ಕೊರವಂಜಿಗೆ ಕಾಲಕ್ರಮೇಣ ಹೊಸ ನಗೆಗಾರರು ಬಂದು ಸೇರಿದರು. ಟಿ.ಸುನಂದಮ್ಮ, ಕೇಫ, ಅ.ರಾ.ಸೇ., ರಾಮಿ, ದಾಶರಥಿ
			ದೀಕ್ಷಿತ್ ಇವರುಗಳು ರಾ.ಶಿ.ಯವರೊಂದಿಗೆ ನಡೆಸುತ್ತಿದ್ದ ಸಂವಾದ, ವಿಚಾರ ವಿನಿಮಯ ಇವುಗಳು ಹಾಸ್ಯಸಾಹಿತ್ಯ ಪ್ರಕಾರದಲ್ಲಿ ಕೊರವಂಜಿಗೆ ಒಂದು ವಿಶಿಷ್ಟ ಸ್ಥಾನವನ್ನು ಗಳಿಸಿಕೊಟ್ಟಿತು. ದಿನನಿತ್ಯದ ಆಗುಹೋಗುಗಳಿಗೆ
			ಸ್ಪಂದಿಸಿ ಬರೆಯುತ್ತಿದ್ದ &ldquo;ಕುಹಕಿಡಿಗಳು&rdquo; ಹಾಗೂ &ldquo;ಉರಿಗಾಳು&rdquo; ಅಂಕಣಗಳು, ರಾ.ಶಿ.ಯವರ ಅರಳು ಹುರಿದಂತಹ ಮಾತುಗಾರಿಕೆಯನ್ನು ಹೋಲುತ್ತಿದ್ದವು. &ldquo;ಕೊರವಾವಲೋಕನ&rdquo; ಸಮಾಜದ ಏರುಪೇರು ಮೌಲ್ಯಗಳನ್ನು
			ಲೇವಡಿಮಾಡಿ ಬರೆದಂತಹ ಕಾಲಂ. ಐವತ್ತರ ದಶಕದಲ್ಲಿ ಕೊರವಂಜಿಗೆ ಯುವ ಬರಹಗಾರರ ಸಮೂಹ ಸೇರ್ಪಡೆಯಾಯಿತು. ಕೊಳ್ಳೇಗಾಲದ್ನಾರಾಮಾನುಜ, ಹಾ.ರಾ, ಹಾರ್ನಳ್ಳಿ ರಾಮಸ್ವಾಮಿ ಇವರುಗಳೇ ಅಲ್ಲದೆ, ಇನ್ನೂ ಅನೇಕ
			ಪ್ರತಿಭಾವಂತ ಲೇಖಕರು ರಾ.ಶಿ.ಯವರ ನಗೆ ಪ್ರಜ್ಞೆಯಿಂದ ಪ್ರಭಾವಿತರಾಗಿ ಕೊರವಂಜಿಗೆ ಬರೆಯಲು ಪ್ರಾರಂಭಿಸಿದರು. ಇಪ್ಪತ್ತೈದು ವರ್ಷಗಳ ಕಾಲ ಸತತವಾಗಿ ಕನ್ನಡದ ನಗೆ ರಸಿಕರನ್ನು ರಂಜಿಸಿ ಕೊನೆಗೆ ೧೯೬೭ರ ಏಪ್ರಿಲ್ ಸಂಚಿಕೆಯೊಂದಿಗೆ ಕೊರವಂಜಿಯ ಪ್ರಕಟಣೆ
			ನಿಲ್ಲಬೇಕಾಯಿತು. ರಾ.ಶಿ.ಯವರು ಹೇಳಿದಂತೆ ಕೊರವಂಜಿ ಕಾಡಿಗೆ ಹೋದಳು.<br />

			<br /><span class="subtitle">ಅಪರಂಜಿಯ ಹುಟ್ಟು</span><br />

			<p>೧೯೮೩ ನೇ ಇಸವಿ. ಇಪ್ಪತ್ತೈದು ವರುಷಗಳಕಾಲ ಕನ್ನಡ ಜನತೆಯನ್ನು ರಂಜಿಸಿ, ನಗಿಸಿದ ಕೊರವಂಜಿ ಕಾಡಿಗೆ ತೆರಳಿ ಹದಿನಾರು ವರ್ಷ ಆಗಿತ್ತು. ರಾ.ಶಿ.ಯವರು ತಮ್ಮ ಸಾರ್ಥಕ ಜೀವಿತದ ಕೊನೆಯ ಹಂತವನ್ನು
			ತಲುಪಿದ್ದರು. ಆ ದಿನಗಳಲ್ಲಿ ಒಂದು ಸಂಜೆ ಅ.ರಾ.ಸೇ.ಯವರು ನಮ್ಮ ಮನೆಗೆ ಬಂದರು. ತಮ್ಮ ಜೊತೆಗೆ ಅವರು ತಮ್ಮ ಗೆಳೆಯ ಶೇಷಗಿರಿಯವರನ್ನೂ ಕರೆತಂದಿದ್ದರು. ಆಲಸ್ಯದಿಂದ ಮಲಗಿದ್ದ ನಮ್ಮ ತಂದೆಯವರ ಬಳಿಗೆ 
			ನಾನೇ ಬಂದ ಅತಿಥಿಗಳನ್ನು ಕರೆದೊಯ್ದೆ. ಪರಸ್ಪರ ಕುಶಲ ಪ್ರಶ್ನೆಗಳ ನಂತರ ಅ.ರಾ.ಸೇ. ಕೊರವಂಜಿಯನ್ನು ಪುನಃ ಹೊರತರುವ ಆಲೋಚನೆಯನ್ನು ಮುಂದಿಟ್ಟರು. ಮೊದಲಿಗೆ ರಾ.ಶಿ.ಯವರು ಅಷ್ಟೇನೂ ಉತ್ಸಾಹ
			ತೋರಿಸದಿದ್ದರೂ, ಕೊನೆಗೆ ಅ.ರಾ.ಸೇ.ಯವರ ಸ್ನೇಹಪೂರ್ಣ ಒತ್ತಾಯಕ್ಕೆ ಮಣಿದು ತಮ್ಮ ಒಪ್ಪಿಗೆ ನೀಡಿದರು. ಒಂದು ಷರತ್ತನ್ನೂ ಹಾಕಿದರು. &ldquo;ಕೊರವಂಜಿ ಅನ್ನೋ ಹೆಸರು ಬೇಡ. ಬೇರೆ ಯಾವುದಾದರೂ
			ಹೆಸರನ್ನಿಡಿ. ಕೊರವಂಜಿ ಪರಂಪರೆ ಇಲ್ಲಿಗೆ ನಿಲ್ಲಲಿ. ಹೊಸ ಪತ್ರಿಕೆ, ತನ್ನದೇ ಆದ ಪರಂಪರೆ ಬೆಳೆಸಲಿ.&rdquo; ಅಂದರು. &ldquo;ಯಾವ ಹೆಸರನ್ನಿಡೋಣ ಸಾರ್?&rdquo; ಅಂತ ಅ.ರಾ.ಸೇ. ಕೇಳಿದ್ದಕ್ಕೆ, &ldquo;ಅಪರಂಜಿ&rdquo; ಎಂಬ
			ಹೆಸರನ್ನು ರಾ.ಶಿ.ಯವರೇ ಸೂಚಿಸಿದರು. ೧೯೮೩ರ ಶ್ರಾವಣ ಮಾಸದಲ್ಲಿ ಅಪರಂಜಿಯ ಮೊದಲ ಸಂಚಿಕೆ ಚಿತ್ರದುರ್ಗದಲ್ಲಿ ಬಿಡುಗಡೆಯಾಯಿತು. ಅಪರಂಜಿಯ ಮೊದಲ ಸಂಪಾದಕರು ಶ್ರೀ ಆರ್.ಶೇಷಗಿರಿ ರಾವ್ ಅವರು. ಮೈಸೂರಿನಲ್ಲಿ ಪಂಚಾಕ್ಷರಿ ಎಂಬುವರ ಮುದ್ರಣಾಲಯದಲ್ಲಿ ಅಪರಂಜಿ
			ಮುದ್ರಣ. ವಿತರಣೆಯನ್ನು ನಾನು ಬೆಂಗಳೂರಿನಿಂದ ಮಾಡುತ್ತಿದ್ದೆ. ಅ.ರಾ.ಸೇ. ನಮಗೆಲ್ಲಾ ಪ್ರಧಾನ ಸಲಹೆಗಾರರು. ಅಂತೂ, ಸುಮಾರು ಎಂಟು ತಿಂಗಳು ಈ ರೀತಿಯಲ್ಲಿ ಚಿತ್ರದುರ್ಗ, ಮೈಸೂರು, ಬೆಂಗಳೂರು ಸರ್ಕಸ್ ನಡೆದ ಮೇಲೆ, &ldquo;ಇದು ಯಾಕೋ ಸರಿಹೋಗಲ್ಲ&rdquo; ಅಂತ ನಿರ್ಧರಿಸಿ
			ಬೆಂಗಳೂರಿನಿಂದಲೇ ಪತ್ರಿಕೆಯನ್ನು ಹೊರತರಲು ನಿರ್ಧರಿಸಿದೆವು. ಪತ್ರಿಕೆಯ ಸಂಪಾದಕತ್ವದ ಭಾರ ನನ್ನ ಮೇಲೆ ಬಿತ್ತು.</p><br />

			<p>ಒಂದು ದೃಷ್ಟಿಯಲ್ಲಿ ಅಪರಂಜಿ ಅದೃಷ್ಟವಂತೆ ಎನ್ನಬಹುದು. ಆಕೆ ಕಾಡಿನಿಂದ ನಾಡಿಗೆ ಬರುವ ಹೊತ್ತಿಗೆ, ಕೊರವಂಜಿ ಕಾಡಿಗೆ ತೆರಳಿ ದಶಕಕ್ಕೂ ಹೆಚ್ಚಿನ ಕಾಲ ಸರಿದಿತ್ತು. ಹಾಗಿದ್ದರೂ ಸಹಾ ಕೊರವಂಜಿಯ ಬರಹಗಾರ ಬಳಗದ
			ಅ.ರಾ.ಸೇ., ಟಿ.ಸುನಂದಮ್ಮ, ರಾಮಿ, ಕೇಫ, ದಾಶರಥಿ ದೀಕ್ಷಿತ್ ಇವರುಗಳ ಸಹಕಾರ ಅಪರಂಜಿಗೆ ದೊರಕಿತು. ಅತ್ಯಂತ ಅಕ್ಕರೆಯಿಂದ ಈ ಹಿರಿಯರು, ಅಪರಂಜಿಗೆ ಲೇಖನಗಳನ್ನಿತ್ತು ಪೋಷಿಸಿದರು. ಕೊರವಂಜಿ ತನ್ನ
			ಕಾಲದಲ್ಲಿ ಒಂದು ನಗೆ ಲೇಖಕರ ಪಡೆಯನ್ನು ಸೃಷ್ಟಿಸಿದಂತೆಯೇ ಅಪರಂಜಿಯೂ ಸಹಾ ನಗು ಮನೋಭಾವದ ಪ್ರಚಾರಕರ ಒಂದು ದಂಡನ್ನೇ ನಿರ್ಮಿಸಿದೆಯೆಂದರೆ ಅದೇನು ಉತ್ಪ್ರೇಕ್ಷೆಯ ಮಾತು ಅನ್ನಿಸದು. ಪ್ರಾರಂಭದಿಂದಲೇ ಅಪರಂಜಿಯ
			ಬಳಗವನ್ನು ಸೇರಿದ ಶ್ರೀನಿವಾಸ ವೈದ್ಯ, ಲೀಲಾ ಮಿರ್ಲೆ, ಎಂ.ಎಸ್.ಕೆ. ಪ್ರಭು, ಶಾಂತಾ ರಘು ಇವರುಗಳೆಲ್ಲಾ ತಮ್ಮ ಉತ್ಕ್ರಷ್ಟ ಹಾಸ್ಯಪ್ರಜ್ಞೆಯಿಂದ ಅಪರಂಜಿಯನ್ನು ಶ್ರೀಮಂತಗೊಳಿಸುವುದರ ಜೊತೆಗೆ ಓದುಗರಲ್ಲಿ ಒಂದು ಉತ್ತಮ ಅಭಿರುಚಿಯನ್ನು ಬೆಳೆಸುವುದಕ್ಕೆ ಕಾರಣರಾದರು.</p>

			<br /><span class="subtitle">ಕೊರವಂಜಿ ಅಪರಂಜಿ ಟ್ರಸ್ಟ್</span><br />

			<p>೧೯೮೪ರಲ್ಲಿ ಸಂಪಾದಕತ್ವ ವಹಿಸಿಕೊಂಡಾಗಿನಿಂದ ಪತ್ರಿಕೆಯ ಎಲ್ಲ ವಹಿವಾಟುಗಳನ್ನೂ ನಾನೇ ನೋಡಿಕೊಳ್ಳಬೇಕಾಗಿತ್ತು. ಸಂಪಾದಕತ್ವದ ಜೊತೆಗೆ ಪ್ರಕಟಣೆ, ವಿತರಣೆ, ಓದುಗರ ಸಮಸ್ಯೆಗಳಿಗೆ ಸ್ಪಂದಿಸುವಿಕೆ ಮುಂತಾದ
			ಸಮಸ್ತ ಜವಾಬ್ದಾರಿಗಳು ನನ್ನ ಹೆಗಲಿಗೆ ಬಿದ್ದವು. ನನ್ನ ಈ ಕಷ್ಟ ನೋಡಲಾರದೇ ಗೆಳೆಯ ನಾಗೇಶ್ ಅವರು, ಪತ್ರಿಕೆಯನ್ನು ನಡೆಸಲು ಒಂದು ಟ್ರಸ್ಟ್ ರಚಿಸಲು ಸೂಚಿಸಿದರು. ೧೯೯೯ನೇ ಇಸವಿಯ ಅಕ್ಟೋಬರ್ ಹದಿನಾಲ್ಕರಂದು ಕೊರವಂಜಿ ಅಪರಂಜಿ ಟ್ರಸ್ಟ್ ನೋಂದಣಿಯಾಗಿ ಅಸ್ತಿತ್ವಕ್ಕೆ
			ಬಂದಿತು. ಗುಂಡೂರಾವ್ ನಾಗೇಶ್, ಬೇಲೂರು ರಾಮಮೂರ್ತಿ, ವಿ.ಆರ್.ನಾಥ್ ಹಾಗೂ ಎಚ್.ಎಸ್.ಕೃಷ್ಣ ಇವರುಗಳು ಸ್ಥಾಪಕ ಟ್ರಸ್ಟಿಗಳಾಗಿ ನನ್ನೊಡನೆ ಅಪರಂಜಿಯನ್ನು ನಡೆಸಲು ಮುಂದಾದರು. ಹಲವು ವರ್ಷಗಳ ನಂತರ ಎಚ್.ಎಸ್.ಕೃಷ್ಣ ಅವರು ವೈಯ್ಯಕ್ತಿಕ ಕಾರಣಗಳಿಂದ ತಮ್ಮ ಟ್ರಸ್ಟಿ
			ಸ್ಥಾನಕ್ಕೆ ರಾಜಿನಾಮೆಯನ್ನಿತ್ತಾಗ ಅವರ ನಿರ್ಗಮನದಿಂದ ತೆರವಾದ ಸ್ಥಾನಕ್ಕೆ, ಎ.ಆರ್.ರಾಜಗೋಪಾಲ್ ಮತ್ತು ಕೆ.ರವೀಂದ್ರರಾವ್ ಅವರನ್ನು ಟ್ರಸ್ಟಿಗಳಾಗಿ ಕಾರ್ಯನಿರ್ವಹಿಸಲು ಆಹ್ವಾನಿಸಲಾಯಿತು. ಅದೇ ಸಮಯದಲ್ಲಿ ಆರ್.ಶೇಷಗಿರಿರಾವ್ ಹಾಗೂ ಎಸ್.ಪ್ರಶಾಂತ್ ರಾವ್ ಇವರನ್ನು ವಿಶೇಷ
			ಆಹ್ವಾನಿತರಾಗಿ ಟ್ರಸ್ಟ್ ನ ಸಭೆಗಳಲ್ಲಿ ಭಾಗವಹಿಸುವಂತೆ ವಿನಂತಿಸಲಾಯಿತು. ಅ.ರಾ.ಸೇ. ಅವರು ನಮ್ಮ ಗೌರವ ಸಲಹೆಗಾರರಾದರು. ಈಗ ಅಪರಂಜಿಗೆ ಇಪ್ಪತ್ತೇಳು ತುಂಬಿ ಇಪ್ಪತ್ತೆಂಟರಲ್ಲಿ ಕಾಲಿಟ್ಟಿದ್ದಾಳೆ. ಬರಹಗಾರ ಬಳಗ ಬಹುವಾಗಿ ಬೆಳೆದಿದೆ. ಕನ್ನಡ ನಾಡಿನ ಸಾಹಿತ್ಯಾಭಿಮಾನಿಗಳು ಅಪರಂಜಿಯನ್ನು ತುಂಬು ಹೃದಯದಿಂದ
			ಬರಮಾಡಿಕೊಂಡಿದ್ದಾರೆ. ಅಂತರ್ಜಾಲದ ಮೂಲಕ ಹೊರನಾಡ ಕನ್ನಡಿಗರನ್ನು ತಲುಪುವ ಆಸೆ ನಮ್ಮದು.</p><br />
